# =============================================================================
#  orientation.py — ОПРЕДЕЛЕНИЕ ОРИЕНТАЦИИ КАРТИНКИ
#
#  Задача модуля: посмотреть на размеры файла-картинки и решить,
#  какое соотношение сторон выбирать в интерфейсе сайта:
#    - ширина БОЛЬШЕ высоты  -> горизонталь -> "16_9"
#    - иначе (включая квадрат) -> вертикаль  -> "9_16"
#
#  Возвращаемые строки ("16_9" / "9_16") совпадают с ключами кнопок
#  в config.py (ratio_16_9 / ratio_9_16), чтобы дальше было удобно.
# =============================================================================

from PIL import Image   # Pillow — библиотека для работы с изображениями


def get_orientation(image_path):
    """
    Открывает картинку и определяет нужную ориентацию.

    Аргумент:
        image_path — путь к файлу картинки (строка).

    Возвращает:
        "16_9" — если картинка горизонтальная (ширина > высоты),
        "9_16" — если вертикальная или квадратная.
    """
    # Открываем файл картинки. Конструкция "with" сама закроет файл после чтения.
    with Image.open(image_path) as img:
        width, height = img.size   # .size отдаёт пару чисел: (ширина, высота)

    # Главное правило проекта:
    if width > height:
        return "16_9"   # горизонталь
    else:
        return "9_16"   # вертикаль (или квадрат)


# -----------------------------------------------------------------------------
#  Блок самопроверки.
#  Запустится ТОЛЬКО если файл запущен напрямую (python orientation.py),
#  а при импорте из других файлов — не сработает.
#  Удобно, чтобы быстро проверить модуль на реальной картинке.
# -----------------------------------------------------------------------------
if __name__ == "__main__":
    import sys

    # Если при запуске указали путь к картинке — берём его,
    # иначе подсказываем пользователю, как пользоваться.
    if len(sys.argv) < 2:
        print("Как пользоваться:")
        print('   python orientation.py "путь\\к\\картинке.jpg"')
    else:
        path = sys.argv[1]
        try:
            result = get_orientation(path)
            # Покажем заодно сами размеры, чтобы было видно логику
            with Image.open(path) as im:
                w, h = im.size
            print(f"Файл: {path}")
            print(f"Размеры: ширина={w}, высота={h}")
            print(f"Ориентация: {result}  "
                  f"({'горизонталь 16:9' if result == '16_9' else 'вертикаль 9:16'})")
        except FileNotFoundError:
            print(f"ОШИБКА: файл не найден -> {path}")
        except Exception as e:
            print(f"ОШИБКА при чтении картинки: {e}")
