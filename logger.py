# =============================================================================
#  logger.py — ПРОСТОЕ ЛОГИРОВАНИЕ: пишем И на экран, И в файл.
#
#  Идея: вместо обычного print(...) мы зовём log(...).
#  Сообщение при этом:
#    - выводится в консоль (как раньше), чтобы ты видел прогресс вживую;
#    - дописывается в файл logs/log_ГГГГ-ММ-ДД.txt с отметкой времени.
#
#  Файл лога НЕ перезаписывается, а ДОПИСЫВАЕТСЯ — за день всё в одном файле.
#  Имя файла берётся из config.LOG_FILE_NAME (там стоит {date} = текущая дата).
# =============================================================================

import os                      # создание папки logs, склейка путей
from datetime import datetime  # текущая дата и время для отметок

import config                  # берём имя файла лога и папку logs


def _log_file_path():
    """Собирает полный путь к сегодняшнему лог-файлу (с подстановкой даты)."""
    # Папка logs может отсутствовать (например, .gitkeep удалили) — создадим на всякий.
    os.makedirs(config.LOGS_DIR, exist_ok=True)

    # Текущая дата в виде 2026-06-14 — подставляем в шаблон имени из config.
    today = datetime.now().strftime("%Y-%m-%d")
    file_name = config.LOG_FILE_NAME.format(date=today)

    return os.path.join(config.LOGS_DIR, file_name)


def log(message):
    """Печатает сообщение на экран И дописывает его в сегодняшний лог-файл."""
    # Отметка времени до секунд — чтобы потом понимать, когда что произошло.
    timestamp = datetime.now().strftime("%H:%M:%S")
    line = f"[{timestamp}] {message}"

    # 1) На экран — как обычный print.
    print(line)

    # 2) В файл. Открываем в режиме "a" (append = дописать в конец).
    #    encoding="utf-8" обязателен из-за русских букв и эмодзи (✅ ❌).
    #    Любую ошибку записи в файл "глотаем": если лог вдруг не пишется,
    #    скрипт всё равно должен продолжать работать (лог — не главное).
    try:
        with open(_log_file_path(), "a", encoding="utf-8") as f:
            f.write(line + "\n")
    except Exception as e:
        # Сообщаем в консоль, но НЕ роняем скрипт из-за проблем с логом.
        print(f"(не удалось записать в лог-файл: {e})")
