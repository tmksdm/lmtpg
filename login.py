# =============================================================================
#  login.py — ОДНОКРАТНЫЙ ВХОД РУКАМИ + СОХРАНЕНИЕ СЕССИИ
#
#  Запускаешь ЭТОТ файл ОДИН раз. Откроется окно браузера на странице входа.
#  Ты вводишь email и пароль РУКАМИ прямо в этом окне и нажимаешь "Войти".
#  Когда окажешься на дашборде — возвращаешься в терминал и жмёшь Enter.
#  Скрипт сохранит "отпечаток" входа в файл storage_state.json.
#
#  ВАЖНО: пароль НИГДЕ не сохраняется. Сохраняются только cookies сессии.
# =============================================================================

from playwright.sync_api import sync_playwright   # движок автоматизации браузера
import config                                     # наши настройки из config.py


def main():
    # Запускаем Playwright
    with sync_playwright() as p:
        # Открываем настоящий видимый браузер Chromium (headless=False = окно видно).
        # Видимое окно нужно, чтобы ты мог ввести логин/пароль руками.
        browser = p.chromium.launch(headless=False)

        # Создаём чистый контекст (как новый профиль браузера без истории)
        context = browser.new_context()

        # Открываем новую вкладку
        page = context.new_page()

        # Идём на страницу входа (адрес берём из config.py)
        print(f"Открываю страницу входа: {config.LOGIN_URL}")
        page.goto(config.LOGIN_URL, timeout=config.PAGE_TIMEOUT_MS)

        # --- ТУТ ДЕЙСТВУЕШЬ ТЫ, РУКАМИ ---
        print("\n" + "=" * 60)
        print("  СЕЙЧАС ВОЙДИ В АККАУНТ РУКАМИ В ОКНЕ БРАУЗЕРА:")
        print("  1) Введи email и пароль")
        print("  2) Нажми 'Войти'")
        print("  3) Дождись, пока откроется дашборд")
        print("  4) Вернись СЮДА, в терминал, и нажми Enter")
        print("=" * 60 + "\n")

        # Скрипт ждёт, пока ты нажмёшь Enter в терминале.
        # Пока ты не нажал — браузер просто стоит открытым и ждёт.
        input("После успешного входа нажми Enter здесь... ")

        # Сохраняем "отпечаток" сессии (cookies, localStorage) в файл.
        # Этот файл потом переиспользуем, чтобы не вводить пароль заново.
        context.storage_state(path=config.STORAGE_STATE_FILE)
        print(f"\nГотово! Сессия сохранена в файл: {config.STORAGE_STATE_FILE}")
        print("Этот файл НЕ попадёт в репозиторий (он в .gitignore).")

        # Закрываем браузер
        browser.close()


# Точка входа: код выполнится только при прямом запуске файла
if __name__ == "__main__":
    main()
